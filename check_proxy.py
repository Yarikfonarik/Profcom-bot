async def check(proxy):
    url = "https://api.telegram.org"
    try:
        connector = ProxyConnector.from_url(f"socks5://{proxy}", rdns=True)
        start = time.monotonic()
        async with aiohttp.ClientSession(connector=connector) as session:
            async with session.get(url, timeout=aiohttp.ClientTimeout(total=8)) as resp:
                if resp.status in (200, 404):
                    ping = int((time.monotonic() - start) * 1000)
                    print(f"✅ {proxy} — {ping}ms")
                    return (ping, proxy)
    except Exception:
        pass
    return None

async def main():
    print(f"Проверяю {len(PROXIES)} прокси...\n")
    tasks = [check(p) for p in PROXIES]
    results = await asyncio.gather(*tasks)
    working = sorted([r for r in results if r], key=lambda x: x[0])
    
    if working:
        print(f"\n🏆 Топ-5 быстрых прокси:")
        for ping, proxy in working[:5]:
            print(f"  {proxy} — {ping}ms")
        print(f"\n✅ Лучший прокси: {working[0][1]} ({working[0][0]}ms)")
        
        # Записываем лучший в файл
        with open("best_proxy.txt", "w") as f:
            f.write(working[0][1])
        print("Сохранён в best_proxy.txt")
    else:
        print("😔 Ни один прокси не работает")

asyncio.run(main())
